package org.pruebas.examenA;

public class Person {
	
	private String firstName;
	private int salary;
	public Person(String firstName, int salary) {
		this.firstName = firstName;
		this.salary = salary;
	}
	public String getFirstName() {
		return firstName;
	}
	
	public int getSalary() {
		return salary;
	}
		
	
	

}
