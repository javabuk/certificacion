package org.pruebas.examenA.ejercicio39;

public class Book {
	
	String bname;
	double price;
	
	public Book (String bname, double price){
		this.bname = bname;
		this.price = price;
	}

	public String getBname() {
		return bname;
	}

	public void setBname(String bname) {
		this.bname = bname;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	
	
}
